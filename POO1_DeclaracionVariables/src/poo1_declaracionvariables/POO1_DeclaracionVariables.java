package poo1_declaracionvariables;

public class POO1_DeclaracionVariables {
    public static void main(String[] args) {
        byte byteOne = 5;
        short shortOne = 7;
        int intOne = 1;
        long longOne = 1111111111;
        char charOne = 'a';
        double doubleOne = 1.5;
        float floatOne = 1.3f;
        String stringOne = "Hola";
        boolean booleanOne = true;
        
        System.out.print(byteOne + "\n");
        System.out.print(shortOne + "\n");
        System.out.print(intOne + "\n");
        System.out.print(longOne + "\n");
        System.out.print(charOne + "\n");
        System.out.print(doubleOne + "\n");
        System.out.print(floatOne + "\n");
        System.out.print(stringOne + "\n");
        System.out.print(booleanOne + "\n");
    }
}
