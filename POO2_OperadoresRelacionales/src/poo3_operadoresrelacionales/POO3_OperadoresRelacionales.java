package poo3_operadoresrelacionales;

public class POO3_OperadoresRelacionales {
    public static void main(String[] args) {
        int val1 = 5;
        int val2 = 2;
        System.out.println(val1 == val2);
        System.out.println(val1 != val2);
        System.out.println(val1 > val2);
        System.out.println(val1 < val2);
        System.out.println(val1 >= val2);
        System.out.println(val1 <= val2);
    }
}
